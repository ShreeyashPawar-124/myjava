package sap.company;

public class CWH_21_WhileLoop {
    public static void main(String[] args) {
        // While Loop
        int i = 0;
        while (i<=5){
            System.out.println(i+" ");
            i++;
        }
        // Quick Quiz
        int j = 100;
        while (j<=200){
            System.out.print(j+" ");
            j++;
        }
        System.out.println();
        System.out.println("End of While Loop");
    }
}
